                                    <h2 dir="ltr" id="t2">Influencer Marketing Trends for 2023</h2>
                                    <p dir="ltr">With the influencer marketing market size expected to grow 14.47% in 2023,
                                        it’s important to stay in the loop on potential trends for 2023 in order to understand
                                        why this growth potential exists.</p>
                                    <h3 dir="ltr">The Rise of User-Generated Content</h3>
                                    <p dir="ltr">The User-Generated Content (UGC) market has been on the rise over the past
                                        few years, and short-video apps like TikTok have only accelerated this. While
                                        user-generated content may not fit into the traditional mold of influencer marketing, it
                                        still requires that advertisers <a
                                            href="https://collabstr.com/hire-influencers/user-generated-content" target="_blank"
                                            rel="noopener">hire UGC creators and influencers</a> to create the content for them,
                                        which ultimately boosts the spending flowing into the influencer marketing market.</p>
                                    <p dir="ltr">Platforms like Instagram and TikTok have also put in a lot of effort to
                                        accommodate advertisers that prefer to leverage their UGC and they’ve done this by
                                        implementing features for <a href="https://collabstr.com/assets/blog/influencer-whitelisting"
                                            target="_blank" rel="noopener">whitelisting</a> and <a
                                            href="https://collabstr.com/assets/blog/dark-posting" target="_blank" rel="noopener">dark
                                            posting</a>.</p>
                                    <p dir="ltr">In a world of branded content, UGC is refreshing as it allows brands to
                                        create personalized advertisements and content that don’t feel like they’ve come
                                        directly from a corporation. This helps the content fit seamlessly into the feeds of
                                        consumers.</p>
                                    <h3 dir="ltr">Influencer Marketing after iOS 14</h3>
                                    <p dir="ltr">In the spring of 2021, Apple released an update that allows consumers to
                                        opt-out of data tracking when using apps like Instagram or TikTok. This was a huge hit
                                        to social media companies that rely on this data for their advertising businesses. Apple
                                        has continued to show that they are only going to continue moving in this direction,
                                        which is a big threat to brands that rely solely on advertising to acquire new
                                        customers.</p>
                                    <p dir="ltr">As mentioned in our <a
                                            href="https://collabstr.com/2022-influencer-marketing-report" target="_blank"
                                            rel="noopener">2022 influencer marketing report</a>, one of the trends we see as a
                                        result of these moves by Apple is advertisers, such as those in the <a
                                            href="https://collabstr.com/assets/blog/direct-to-consumer-d2c-marketing" target="_blank"
                                            rel="noopener">D2C</a> and <a href="https://collabstr.com/assets/blog/cpg-marketing"
                                            target="_blank" rel="noopener">CPG</a> spaces, allocating more of their budgets
                                        towards influencer marketing in order to diversify advertising channels and create more
                                        of a relationship with their audience.</p>
                                    <h3 dir="ltr">Influx of Micro-Influencers</h3>
                                    <p dir="ltr">As short-video apps continue to dominate the social media landscape there
                                        has been an influx of <a
                                            href="https://collabstr.com/assets/blog/micro-influencers-the-most-powerful-addition-to-your-marketing-strategy"
                                            target="_blank" rel="noopener">micro-influencers</a> in every niche imaginable.
                                    </p>
                                    <p dir="ltr">Short-video platforms like TikTok are known for their viral aspect. A big
                                        part of the reason these platforms are able to attract so many content creators is the
                                        fact that they offer more organic exposure to content creators than traditional methods,
                                        such as posting photos on Instagram.</p>
                                    <p dir="ltr">As a result of this, everybody is becoming a content creator, and with
                                        this, you get more people that become influential in their associated niche.
                                        Micro-influencers can loosely be defined as anyone with a following of less than
                                        50,000.&nbsp;</p>
                                    <p dir="ltr">With more micro-influencers on the market than ever before, there is more
                                        selection for advertisers and brands that are looking for someone to be the face of
                                        their product.&nbsp;</p>
                                    <h2 dir="ltr" id="t3">Most Popular Social Media Platforms for Influencer
                                        Marketing in 2023</h2>
                                    <p dir="ltr">In 2022, TikTok was the most popular social media platform for influencer
                                        marketing, with 45% of paid collaborations taking place on the platform. Instagram comes
                                        second, with 39% of paid influencer collaborations in 2022 taking place on the
                                        platform.&nbsp;</p>
                                    <p dir="ltr"><a href="https://collabstr.com/2022-influencer-marketing-report#t3"
                                            target="_blank" rel="noopener">In last year’s report</a>, we observed that
                                        Instagram was still king when it came to influencer marketing, but TikTok was quickly
                                        gaining momentum, and it was expected that it would soon overtake Instagram as the go-to
                                        platform for influencer marketing.</p>
                                    <p dir="ltr">We also included user-generated content (UGC) in this year’s breakdown
                                        due to its incredible rate of growth, and that brands are allocating budgets
                                        specifically for UGC, just as they would for a platform like TikTok or Instagram. We
                                        observed that 14% of all paid collaborations were not specific to any particular
                                        platform, but instead they were UGC collaborations where the influencer delivered some
                                        content in exchange for a payment. The number of UGC collaborations in 2022 is higher
                                        than we’ve ever observed in the past.</p>
                                    <p dir="ltr">We observed that 2% of paid collaborations took place on YouTube in 2022.
                                        While this may seem low, it’s also important to note that YouTube differs dramatically
                                        from platforms like TikTok and Instagram, where there are much more creators and content
                                        is not as evergreen. YouTube influencers tend to invest more time into the production of
                                        their content, and the content typically stays relevant for much longer, which leads to
                                        higher prices for collaborations.</p>
                                    <p dir="ltr">This data was extrapolated from 7,000 paid collaborations between
                                        influencers and advertisers. In order to qualify as a valid data point, we only analyzed
                                        real orders where we could confirm that there was a monetary exchange between the
                                        influencer and the brand. We discarded gifted collaborations, survey data, and any other
                                        collaborations where there was no exchange of money for the service. We believe that
                                        analyzing where the budget is being spent, rather than where people talk about spending
                                        their budget, is the best way to get an accurate look at the landscape.</p>
