                                    <h3 dir="ltr">Instagram Influencer Cost: $363</h3>
                                    <p dir="ltr">Instagram influencers charge $363 on average for a collaboration. This
                                        rate includes content packages where influencers offer static feed posts, reels, and
                                        stories. Prices also fluctuate based on add-ons, such as the utilization of the new <a
                                            href="https://collabstr.com/assets/blog/instagram-collab-post" target="_blank"
                                            rel="noopener">Instagram collab post feature</a>. The average price per content
                                        package is lower on Instagram than it is on TikTok, this could be for a few reasons.</p>
                                    <p dir="ltr">First off, there are more influencers on Instagram than on any other
                                        platform, and when there is a surplus of supply, the prices naturally decline.</p>
                                    <p dir="ltr">Next, Instagram content takes less time to create than TikTok or YouTube
                                        content. Influencer marketing on Instagram is still heavily reliant on static photo
                                        posts and Instagram stories. On the other hand, TikTok and YouTube are video apps that
                                        do not support photo content, and video can require more time and effort to film and
                                        edit hence the higher price tag.</p>
                                    <p dir="ltr">Lastly, Instagram does not offer the organic exposure that other
                                        platforms do. TikTok is known for rewarding creators with millions of views, and YouTube
                                        is an evergreen platform in the sense that it will continue to push your content years
                                        after it has been posted. On the other hand, Instagram has a weak discovery algorithm
                                        which has only recently improved due to the introduction of Reels.</p>
                                    <h3 dir="ltr">User-Generated Content Influencer Cost: $396</h3>
                                    <p dir="ltr">User-generated content (UGC) creators charge $396 on average for a
                                        collaboration. We analyzed thousands of UGC packages from our creators to figure out the
                                        average amount that is charged by them. Some packages may include multiple pieces of
                                        content, while other packages may only include a single piece of content.&nbsp;</p>
                                    <p dir="ltr">UGC can mean a variety of things such as product photos, ads,
                                        testimonials, unboxing videos, and more. Creators have become increasingly interested in
                                        charging brands for this service as more and more brands are looking for high-quality
                                        custom content for their products.</p>
                                    <p dir="ltr">Typically with UGC, usage rights are sold alongside the content package
                                        so that the buyer is granted permission to use the content exclusively, whether that be
                                        on their website, in their ads, or on their own social media channels. This can
                                        contribute to a higher average price per UGC package.</p>
                                    <h3 dir="ltr">TikTok Influencer Cost: $460</h3>
                                    <p dir="ltr">TikTok influencers charge $460 on average for a collaboration. Despite
                                        TikTok being a relatively new platform, influencers seem to be pricing themselves on the
                                        higher end.&nbsp;</p>
                                    <p dir="ltr">Higher pricing could be due to the fact that TikTok is new, so there
                                        aren’t as many influencers as on Instagram. <a
                                            href="https://collabstr.com/assets/blog/tiktok-statistics" target="_blank"
                                            rel="noopener">TikTok statistics</a> show that TikTok was the most downloaded
                                        social media app in the first quarter of 2022, so this could change soon.</p>
                                    <p dir="ltr">TikTok content can also be time-consuming to film and edit since a
                                        collaboration on TikTok almost always means you’ll be filming a 60-second video.
                                        Increased labor means an increased cost.</p>
                                    <h3 dir="ltr">YouTube Influencer Cost: $791</h3>
                                    <p dir="ltr">YouTube influencers charge $791 on average for a collaboration. It comes
                                        as no surprise that influencer prices are the highest on YouTube as it’s the most
                                        time-consuming and labor-intensive platform for creating content as an influencer.</p>
