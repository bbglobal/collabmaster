                                    <p dir="ltr">On average, UGC creators make $227 per collaboration. While most UGC
                                        creators are not required to post the content to their social channels, they end up
                                        making money in addition to the content they deliver by offering usage rights.</p>
                                    <p dir="ltr">Brands will pay UGC creators extra in order to purchase exclusive rights
                                        to their content, and some creators can charge more depending on how long the brand
                                        would like the rights for.</p>
                                    <h3 dir="ltr">YouTube Influencers Make $418 Per Collaboration</h3>
                                    <p dir="ltr">On average, YouTube influencers make $418 per collaboration. YouTube is
                                        the highest-paying social media platform per brand deal. This is due to the fact that
                                        YouTube content is labor-intensive to produce, and it also stays relevant for a much
                                        longer time than content on platforms like TikTok or Instagram.</p>
