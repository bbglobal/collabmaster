<script>
    document.addEventListener("DOMContentLoaded", function() {
        const dropdownInputs = document.querySelectorAll(".dropdown-input");
        const dropdownLists = document.querySelectorAll(".dropdown-list");

        // Function to close all dropdowns except the one with a specific index
        function closeAllDropdownsExcept(indexToKeepOpen) {
            dropdownLists.forEach((list, index) => {
                if (index !== indexToKeepOpen) {
                    list.style.display = "none";
                }
            });
        }

        // Add click event listeners to open the dropdowns
        dropdownInputs.forEach((input, index) => {
            input.addEventListener("click", function(event) {
                event.stopPropagation();
                closeAllDropdownsExcept(
                    index); // Close all dropdowns except the one being opened
                dropdownLists[index].style.display =
                    "block"; // Show the corresponding dropdown list
            });

            // Select both <li> and <p> elements within the dropdown list
            const listItems = dropdownLists[index].querySelectorAll("li, p");
            listItems.forEach((item) => {
                item.addEventListener("click", function() {
                    input.value = this.textContent;
                    dropdownLists[index].style.display =
                        "none"; // Hide only the dropdown list associated with the clicked input
                });
            });

            // Add a click event listener to close the dropdown when clicking outside
            document.addEventListener("click", function(event) {
                if (!input.contains(event.target)) {
                    dropdownLists[index].style.display =
                        "none"; // Hide only the dropdown list associated with the clicked input
                }
            });

            // Prevent the click event from propagating to the document
            input.addEventListener("click", function(event) {
                event.stopPropagation();
            });
        });

        $(document).on("click", '.zoom-in-image', function() {
            var image = this;
            var modalImage = document.getElementById("modalImage");
            modalImage.src = image.src; // Set the source of the imageModal image
        });

    });

    $('.content-vid').click(function() {
        if ($(this).prop('paused')) {
            $(this).trigger('play');
            $(this).parent().find('.play-btn').css('visibility', 'hidden');
        } else {
            $(this).trigger('pause');
            $(this).parent().find('.play-btn').css('visibility', 'visible');
        }
    });
</script>
